# Variables - To store data or values
a = 5 #Integer
b = "Time" #String
c = 3.25 #Float
'''Format for Variables are it begins with character and can end with a number - there are no spaces in between and it cannot start with a number. We can use underscore (_) in between characters to create a variable for more readability'''

print(a)
print(b)
print(c)

'''type (function) is a way to check the type of a variable'''

print(type(a))
print(type(b))
print(type(c))

